class C 
{
	public static void main(String[] args) 
	{
		System.out.println(Integer.toBinaryString(145));
		System.out.println(Integer.toBinaryString(85));
		System.out.println(145 | 85 ); // binary or
		System.out.println(145 & 85 ); // binary and
		System.out.println(145 ^ 85 ); // binary xor
		
		
	}
}
