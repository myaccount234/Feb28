class D
{
	public static void main(String[] args) 
	{
		System.out.println(Integer.toBinaryString(950));
		System.out.println(Integer.toBinaryString(38));
		System.out.println(950 | 38 ); // binary or
		System.out.println(950 & 38 ); // binary and
		System.out.println(950 ^ 38 ); // binary xor
		
		
	}
}
